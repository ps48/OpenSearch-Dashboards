'                  React.createElement(eui.EuiFlexItem, { grow: false }, pct + "%"),',
'                  React.createElement(eui.EuiFlexItem, null, React.createElement("div", { style: { width: pct + "%", maxWidth: "150px", height: "8px", background: "#006BB4", borderRadius: "4px" } }))',
