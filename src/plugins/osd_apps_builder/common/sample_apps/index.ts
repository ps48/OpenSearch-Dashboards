'var React = require("react");',
'var eui = require("@elastic/eui");',
'',
'exports.default = function WebLogsExplorer(props) {',
'  var api = props.api;',
'  var useState = React.useState;',
'  var useEffect = React.useEffect;',
'  var useCallback = React.useCallback;',
'',
'  var _logs = useState([]);',
'  var logs = _logs[0]; var setLogs = _logs[1];',
'  var _loading = useState(true);',
'  var loading = _loading[0]; var setLoading = _loading[1];',
'  var _filter = useState("all");',
'  var filter = _filter[0]; var setFilter = _filter[1];',
'  var _stats = useState({ total: 0, bytes: 0, errors: 0 });',
'  var stats = _stats[0]; var setStats = _stats[1];',
'',
'  var fetchLogs = useCallback(function() {',
'    setLoading(true);',
'    var query = "source=opensearch_dashboards_sample_data_logs | sort timestamp desc | head 50";',
'    api.search({ query: query, language: "PPL" })',
'      .then(function(res) {',
'        var hits = res && res.jsonData ? res.jsonData : [];',
'        setLogs(hits);',
'        var totalBytes = hits.reduce(function(s, h) { return s + (h.bytes || 0); }, 0);',
'        var errorCount = hits.filter(function(h) { var r = String(h.response || ""); return r.charAt(0) === "4" || r.charAt(0) === "5"; }).length;',
'        setStats({ total: hits.length, bytes: totalBytes, errors: errorCount });',
'        setLoading(false);',
'      })',
'      .catch(function() { setLogs([]); setLoading(false); });',
'  }, [api]);',
'',
'  useEffect(function() { fetchLogs(); }, [fetchLogs]);',
'',
'  var getResponseColor = function(code) {',
'    var c = String(code).charAt(0);',
'    if (c === "2") return "secondary";',
