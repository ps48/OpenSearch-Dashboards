'    ),',
'    React.createElement(eui.EuiText, { size: "xs", color: "subdued" }, "Index: opensearch_dashboards_sample_data_logs | Query: PPL"),',
'    React.createElement(eui.EuiSpacer, { size: "m" }),',
'    React.createElement(eui.EuiFlexGroup, { gutterSize: "l" },',
'      React.createElement(eui.EuiFlexItem, null, React.createElement(eui.EuiPanel, { paddingSize: "m", hasBorder: true }, React.createElement(eui.EuiStat, { title: String(stats.total), description: "Total Hits", titleColor: "primary" }))),',
