'      React.createElement(eui.EuiFlexItem, null, React.createElement(eui.EuiPanel, { paddingSize: "m", hasBorder: true }, React.createElement(eui.EuiStat, { title: String(stats.errors), description: "Errors (4xx/5xx)", titleColor: stats.errors > 0 ? "danger" : "secondary" })))',
